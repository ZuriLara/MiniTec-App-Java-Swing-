private void iniciarCarrusel() {
    // Configuramos un timer que se ejecute cada 3000ms (3 segundos)
    timerCarrusel = new javax.swing.Timer(3000, (java.awt.event.ActionEvent e) -> {
        // Aumentar el índice y volver a 0 si llegamos al final
        indiceActual = (indiceActual + 1) % promos.length;
        
        // Actualizar el diseño del panel que creaste en NetBeans
        pnlCarousel.setBackground(colores[indiceActual]);
        lblPromo.setText(promos[indiceActual]);
        
        // Efecto de refresco
        pnlCarousel.revalidate();
        pnlCarousel.repaint();
    });
    
    timerCarrusel.start(); // ¡Arranca el movimiento!
}

