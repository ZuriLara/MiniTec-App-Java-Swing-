private void personalizarDiseno() {
    // Configuración del Grid de productos (2 columnas, espacio de 10px)
    pnlGrid.setLayout(new java.awt.GridLayout(0, 2, 10, 10));
    pnlGrid.setBackground(new java.awt.Color(240, 240, 240));

    // Agregamos algunos productos de prueba
    agregarProducto("Hamburguesa Doble", "$ 8.50");
    agregarProducto("Pizza Familiar", "$ 12.00");
    agregarProducto("Sushi Roll (10pcs)", "$ 9.90");
    agregarProducto("Tacos al Pastor", "$ 5.00");
    agregarProducto("Ensalada Caesar", "$ 6.50");
    agregarProducto("Ramen Especial", "$ 11.00");

    // Refrescar el panel para que NetBeans muestre los cambios
    pnlGrid.revalidate();
    pnlGrid.repaint();
}

// 2. Método para generar las "tarjetas" de comida dinámicamente
private void agregarProducto(String nombre, String precio) {
    // Creamos un panel pequeño para cada producto
    javax.swing.JPanel card = new javax.swing.JPanel();
    card.setLayout(new java.awt.BorderLayout());
    card.setBackground(java.awt.Color.WHITE);
    card.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(220, 220, 220)));

    // Espacio para la imagen (Icono)
    javax.swing.JLabel lblImagen = new javax.swing.JLabel("[Imagen]");
    lblImagen.setHorizontalAlignment(javax.swing.SwingConstants.CENTER);
    lblImagen.setPreferredSize(new java.awt.Dimension(100, 100));
    
    // Panel de texto (Nombre y Precio)
    javax.swing.JPanel pnlInfo = new javax.swing.JPanel();
    pnlInfo.setLayout(new java.awt.GridLayout(2, 1));
    pnlInfo.setBackground(java.awt.Color.WHITE);
    pnlInfo.setBorder(javax.swing.BorderFactory.createEmptyBorder(5, 5, 5, 5));

    javax.swing.JLabel lblNombre = new javax.swing.JLabel(nombre);
    lblNombre.setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 12));
    
    javax.swing.JLabel lblPrecio = new javax.swing.JLabel(precio);
    lblPrecio.setForeground(new java.awt.Color(0, 153, 51)); // Verde Mercado Libre

    pnlInfo.add(lblNombre);
    pnlInfo.add(lblPrecio);

    card.add(lblImagen, java.awt.BorderLayout.CENTER);
    card.add(pnlInfo, java.awt.BorderLayout.SOUTH);

    pnlGrid.add(card);
}
